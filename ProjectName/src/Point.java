public class Point {
	private int x, y, z;

	public Point() {
		setX(0);
		setY(0);
		setZ(0);
	}

	public Point(int x, int y, int z) {
		setX(x);
		setY(y);
		setZ(z);
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getZ() {
		return z;
	}

	public void setZ(int z) {
		this.z = z;
	}

	public String toString() {
		return String.format("(%d, %d, %d)", x, y, z);
	}
}
